/**
 * Pre-baked academic-talk scripts users can read for voice cloning when
 * they'd rather skip the LLM call. Each one mimics presenting a real
 * (but synthesised) paper: opener → method → result → take. ~1000 字
 * each so the recording lands in the 4-5 minute sweet spot.
 *
 * Why hand-written: the LLM path costs ~4K tokens per generation; users
 * who just want a clean sample shouldn't have to spend tokens, and the
 * talks here are deliberately broad enough to fit any research area.
 */
